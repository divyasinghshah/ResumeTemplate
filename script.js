function about(){

	targetPos=300;
	currentPos=0;

	let scrollInterval=setInterval(function(){
		if(currentPos>=targetPos)
		{
			clearInterval(scrollInterval);
			return;
		}

		currentPos+=50;
		window.scrollBy(0,50);

	},15);
}


